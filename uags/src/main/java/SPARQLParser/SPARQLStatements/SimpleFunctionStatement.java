public class SimpleFunctionStatement extends SimpleStatement {
    /**
     * default constructor
     * <p>
     * since a simple statement is just a string you can pass it here already
     *
     * @param statement
     */
    public SimpleFunctionStatement(String statement) {
        super(statement);
    }

    public String toString()
    {
        return super.toString();
    }
}
