package delta_service.graphs;

public class Graph {
	private String graph;
	private int priority;
	private String updateFiler;

	public String getGraph() {
		return graph;
	}

	public void setGraph(String graph) {
		this.graph = graph;
	}

	public int getPriority() {
		return priority;
	}

	public void setPriority(int priority) {
		this.priority = priority;
	}

	public String getUpdateFiler() {
		return updateFiler;
	}

	public void setUpdateFiler(String updateFiler) {
		this.updateFiler = updateFiler;
	}
	
	

}
